package varios;

public class XMLArticuloAEnviar
{
	private long cod;
	private int cant;
	private int idPedido;
	
	public int getCant() {
		return cant;
	}
	public void setCant(int cant) {
		this.cant = cant;
	}
	public long getCod() {
		return cod;
	}
	public void setCod(long codArt) {
		this.cod = codArt;
	}
	public int getIdPedido() {
		return idPedido;
	}
	public void setIdPedido(int idPedido) {
		this.idPedido = idPedido;
	}
}
